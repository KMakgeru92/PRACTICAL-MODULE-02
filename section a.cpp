#include <iostream>
using namespace std;

int main() {
    int age;          // declare
    age = 20;         // assign

    double salary = 3500.00; // declare + initialize
    char grade = 'A';        // declare + initialize

    cout << "Age: " << age << endl;
    cout << "Salary: " << salary << endl;
    cout << "Grade: " << grade << endl;

    return 0;
}
